    private void atualizarListaProdutos() {
        DefaultTableModel model = (DefaultTableModel) jtprodutos.getModel();
        model.setRowCount(0); // Limpa a tabela

        List<Produto> produtos = sistema.getTodosProdutos(); // Agora usando o novo método.
        for (Produto produto : produtos) {
            model.addRow(new Object[]{produto.getNomeProduto()}); // Adiciona os nomes dos produtos à tabela.
        }
    }

    
