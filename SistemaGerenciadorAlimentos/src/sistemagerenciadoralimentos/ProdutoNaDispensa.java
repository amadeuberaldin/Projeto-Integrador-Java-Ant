    private void jbtVizualizarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtVizualizarProdutoActionPerformed
        int selectedRow = jtprodutos.getSelectedRow();
        if (selectedRow >= 0) {
            String nomeProduto = (String) jtprodutos.getValueAt(selectedRow, 0);
            Produto produto = sistema.buscarProdutoPorNome(nomeProduto);
            if (produto != null) {
                Produtos telaProdutos = new Produtos(sistema, produto);
                telaProdutos.setLocationRelativeTo(null);
                telaProdutos.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Produto não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto primeiro.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jbtVizualizarProdutoActionPerformed

